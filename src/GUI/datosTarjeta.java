    private void edMesFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_edMesFocusLost
        // TODO add your handling code here:
        if (edAV.getText().length() > 0){
            try {
                lblCheckFecha.setIcon(Utils.returnCheck(Utils.esValidoFecha(edAV.getText(), edMes.getText())));
            } catch (IOException ex) {
                Logger.getLogger(datosTarjeta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_edMesFocusLost

