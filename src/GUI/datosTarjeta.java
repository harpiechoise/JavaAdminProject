    private void edNumeroTarjetaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_edNumeroTarjetaFocusLost
        // TODO add your handling code here:

        if (edNumeroTarjeta.getText().length() > 1){
            ImageIcon cardPick = Utils.tarjetaLinkMaker(edNumeroTarjeta.getText());
            lblTarjetaIcon.setIcon(cardPick);
        }else{
            try {
                lblTarjetaIcon.setIcon(Utils.returnDefault());
            } catch (IOException ex) {
                Logger.getLogger(datosTarjeta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        try {
            lblCheckTarjeta.setIcon(Utils.returnCheck(Utils.esValidoNumero(edNumeroTarjeta.getText())));
        } catch (IOException ex) {
            Logger.getLogger(datosTarjeta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_edNumeroTarjetaFocusLost

